/** 12.U32 #1: порог различия для денежных сумм. Калькулятор делает
    N×M умножений (риск-факторы × buffer × inflation × seasonal × schedule
    × contingency × VAT) — float-rounding накапливается. Любое значение
    с `Math.abs(v) < EPSILON_KOPECK` визуально и финансово = 0 (полкопейки).
    Использование: `Math.abs(cell.value) < EPSILON_KOPECK ? '—' : money(cell.value)`
    вместо строгого `cell.value === 0` — отделяет истинный ноль от
    float-артефактов вроде `1e-15`. Соответствует IEEE 754 §6.2 и общей
    практике финансовых вычислений. */
export const EPSILON_KOPECK = 0.005;

/** Считать сумму в рублях «нулевой» с допуском полкопейки. */
export function isZeroMoney(value) {
    return !Number.isFinite(value) || Math.abs(value) < EPSILON_KOPECK;
}

