/**
 * 12.U12: per-resource множители размера стенда от ПРОМ (по умолчанию).
 * Структура: { STAND: { CPU, GPU, RAM, SSD, HDD, S3 } }. Для PROD все = 1.00.
 * Для остальных — каждый ресурс наследует общий стенд-ратио (`DEFAULT_STAND_SIZE_RATIO`),
 * чтобы новый расчёт по умолчанию вёл себя ИДЕНТИЧНО старой однозначной модели.
 * Пользователь может уточнить в Опроснике любую ячейку независимо от других.
 */
function _buildDefaultResourceRatio() {
    /* 12.U17: читаем значения напрямую из DEFAULT_STAND_SIZE_RATIO, а НЕ из
       захардкоженного литерала. Иначе при правке стенд-ratio дефолты
       per-resource ratio останутся стейл — мы это уже однажды получили
       (объявление было `{ DEV: 0.15, ... }` отдельно от настоящего источника).
       Теперь оба значения берутся из единого места. */
    const out = {};
    for (const stand of ['DEV', 'IFT', 'PSI', 'LOAD', 'PROD']) {
        const standRatio = DEFAULT_STAND_SIZE_RATIO[stand];
        out[stand] = {};
        for (const resource of ['CPU', 'GPU', 'RAM', 'SSD', 'HDD', 'S3']) {
            out[stand][resource] = standRatio;
        }
    }
    return out;
}
export const DEFAULT_RESOURCE_RATIO = Object.freeze(_buildDefaultResourceRatio());

